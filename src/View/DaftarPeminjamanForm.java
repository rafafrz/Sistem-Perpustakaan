    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText().trim();
        List<Model.Peminjaman> daftarPeminjaman = controller.cariPeminjaman(nama);

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) TabelPeminjaman.getModel();
        model.setRowCount(0); 

        for (Model.Peminjaman p : daftarPeminjaman) {
            Object[] rowData = {
                p.getIdBuku(),
                p.getNamaPeminjam(),
                p.getJudulBuku(),
                p.getTanggalPinjam(),
                p.getTanggalKembali()
            };
            model.addRow(rowData);
        }
    }//GEN-LAST:event_btnCariActionPerformed

