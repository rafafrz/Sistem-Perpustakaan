    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
            int selectedRow = TabelPeminjaman.getSelectedRow();
            if (selectedRow >= 0) {
                String judul = TabelPeminjaman.getValueAt(selectedRow, 2).toString();

                int confirm = JOptionPane.showConfirmDialog(this,
                        "Apakah Anda yakin ingin menghapus peminjaman buku dengan judul \"" + judul + "?",
                        "Konfirmasi Hapus",
                        JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                    int id = Integer.parseInt(TabelPeminjaman.getValueAt(selectedRow, 0).toString()); 
                    controller.HapusPeminjaman(id); 
                    tampilkanDataPeminjaman(); 
                }
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

