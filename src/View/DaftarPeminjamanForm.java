     private void tampilkanDataPeminjaman() {
        String[] kolom = { "ID", "Nama Peminjam", "Judul Buku", "Tanggal Pinjam", "Tanggal Kembali" };
        DefaultTableModel model = new DefaultTableModel(kolom, 0);

        List<Peminjaman> daftar = controller.getAllPeminjaman();
        for (Peminjaman data : daftar) {
            Object[] row = {
                data.getIdBuku(),
                data.getNamaPeminjam(),
                data.getJudulBuku(),
                data.getTanggalPinjam(),
                data.getTanggalKembali()
            };
            model.addRow(row);
        }

        TabelPeminjaman.setModel(model);
    }

